    private void btnResActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblDetails.getSelectedRow();
        //
        
        //
        if (selectedRow == -1){
        	JOptionPane.showMessageDialog(null,"Please select a row","Warning",JOptionPane.WARNING_MESSAGE );//不选应报错
        	return;
        }
        //
        String flightID =  (String) tblDetails.getValueAt(selectedRow, 0);
        String row = (String) boxRow.getSelectedItem();
        String col = (String) boxCol.getSelectedItem();
        String customerID = txtCusID.getText();
        if("".equals(customerID)||customerID == null){
        	JOptionPane.showMessageDialog(null,"Please input  customerID","Warning",JOptionPane.WARNING_MESSAGE );//不填应报错
        	return;
        }
        for(Flight fl : msschedule.getMsschedule()){
        	if(fl.getFlightID().equals(flightID)){
        		for(Seat seat:fl.getSeatls()){
        			if(col.equals(""+seat.getColumn())&&row.equals(""+seat.getRow())){
        				if("".equals(seat.getCustomerID())||seat.getCustomerID() == null){
        					Customer cus = new Customer();
        	        		cus.setCustomerID(customerID);
        	        		seat.setCustomer(cus);
        	        		seat.setCustomerID(customerID);
        	        		JOptionPane.showMessageDialog(null,"success!");
        	        		return;
        				}else{
        					JOptionPane.showMessageDialog(null,"This seat has been already reserved!","Warning",JOptionPane.WARNING_MESSAGE );//座位已被预定应报错
            	        	return;
        				}
        			}
        		}
    			JOptionPane.showMessageDialog(null,"Seat not exist!","Warning",JOptionPane.WARNING_MESSAGE );//未找到座位应报错
    	        return;
    		}
        }
        JOptionPane.showMessageDialog(null,"error!","Warning",JOptionPane.WARNING_MESSAGE );
    }//GEN-LAST:event_btnResActionPerformed


