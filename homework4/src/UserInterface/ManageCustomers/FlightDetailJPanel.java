    private void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel) tblDetails.getModel();
        dtm.setRowCount(0);
        for(Flight flight:msschedule.getMsschedule()){
            Object[] row = new Object[5];
            row[0] = flight.getFlightID();
            row[1] = flight.getTakeoffairport();
            row[2] = flight.getArriveairport();
            row[3] = flight.getTakeoffdate();
            row[4] = flight.getTakeofftime();;
            dtm.addRow(row);
        }
    }
    
