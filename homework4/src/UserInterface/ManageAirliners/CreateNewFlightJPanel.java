    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        String flightID = txtFlightID.getText();
        String takeoffairport = txtTakeOffAirport.getText();
        String arrivalairport = txtArrivalAirport.getText();
        String takeoffdate = txtTakeOffDate.getText();
        String takeofftime = txtTakeOffTime.getText();
        String serialNum = txtSerialNum.getText();
        String modelNum = txtModelNum.getText();
        String seatsNum = txtSeatsNum.getText();
        String manufacturer = txtManu.getText();
        String manufactureYear = txtManuYear.getText();
        String airlinerName = txtAirliner.getText();
        try{
            int number1 = Integer.parseInt(manufactureYear);
            int number2 = Integer.parseInt(seatsNum);
            int number3 = Integer.parseInt(modelNum);
            Date dateTime = new Date();
            SimpleDateFormat d1 = new SimpleDateFormat("yyyy-MM-dd");
            dateTime = d1.parse(takeoffdate);
            SimpleDateFormat d2 = new SimpleDateFormat("HH:mm");
            Date timeTime = new Date();
            timeTime = d2.parse(takeofftime);
        }
        catch (ParseException e) { 
            JOptionPane.showMessageDialog(null, "date format:yyyy-MM-dd time format:HH:mm");
            return;
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "please enter with right format");
            return;
        }
        if("".equals(takeoffairport)){
        	JOptionPane.showMessageDialog(null, "Please input a takeoffairport!");
            return;
        }
        if("".equals(flightID)){
        	JOptionPane.showMessageDialog(null, "Please input a flightID!");
            return;
        }
        if("".equals(arrivalairport)){
        	JOptionPane.showMessageDialog(null, "Please input an arrivalairport!");
            return;
        }
        if("".equals(takeoffdate)){
        	JOptionPane.showMessageDialog(null, "Please input a takeoffdate!");
            return;
        }
        if("".equals(takeofftime)){
        	JOptionPane.showMessageDialog(null, "Please input a takeofftime!");
            return;
        }
        if("".equals(serialNum)){
        	JOptionPane.showMessageDialog(null, "Please input a serialNum!");
            return;
        }
        if("".equals(modelNum)){
        	JOptionPane.showMessageDialog(null, "Please input a modelNum!");
            return;
        }
        if("".equals(seatsNum)){
        	JOptionPane.showMessageDialog(null, "Please input a seatsNum!");
            return;
        }
        if("".equals(manufacturer)){
        	JOptionPane.showMessageDialog(null, "Please input a manufacturer!");
            return;
        }
        if("".equals(manufactureYear)){
        	JOptionPane.showMessageDialog(null, "Please input a manufactureYear!");
            return;
        }
        String[] flightIDList = new String[100];
            for(int i = 0 ; i < flschedule.getFlschedule().size();i++){
                flightIDList[i] = flschedule.getFlschedule().get(i).getFlightID();
            }
            
            for(int i = 0; i< flightIDList.length; i++){
                String flightIDtemp = flightIDList[i];
                if(Arrays.asList(flightID).indexOf(flightIDtemp)<0){  
                    Flight flight = flschedule.addFlight();
                    flight.setFlightID(flightID);
                    flight.setTakeoffairport(takeoffairport);
                    flight.setSerialNumber(serialNum);
                    flight.setTakeoffdate(takeoffdate);
                    flight.setArriveairport(arrivalairport);
                    flight.setTakeofftime(takeofftime);
                    Airplane airp = new Airplane();
                    airp.setAirlinerName(airlinerName);
                    airp.setManufacturedYear(Integer.parseInt(manufactureYear));
                    airp.setManufacturer(manufacturer);
                    airp.setModelNumber(Integer.parseInt(modelNum));
                    airp.setSeatnum(Integer.parseInt(seatsNum));
                    //
                    airp.setSerialNumber(serialNum);//Airplane和flight的序列号应一致
                    //
//                    ArrayList<Seat> seatls= new ArrayList<Seat>();
//                    
//                    flight.setSeatls(seatls);
                    flight.setAirplane(airp);
                    msschedule.addFlights(flight);
                    JOptionPane.showMessageDialog(null, "Flight successfully created");
                    break;
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "this flight has been created");
                    break;
                }
            }
            //msschedule.addMsSchedule(flschedule.getFlschedule());
            
    }//GEN-LAST:event_btnCreateActionPerformed


