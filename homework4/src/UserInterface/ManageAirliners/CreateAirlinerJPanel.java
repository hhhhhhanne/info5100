    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        String airlinerName = txtAirlinerName.getText();
        String airlinerAddress = txtAirlinerAddress.getText();
        
        String[] name = new String[100];
            for(int i = 0 ; i < airlinerList.getAirlinerList().size();i++){
                name[i] = airlinerList.getAirlinerList().get(i).getAirlinerName();
            }
            
            for(int i = 0; i< name.length; i++){
                String nametemp = name[i];
                if(Arrays.asList(airlinerName).indexOf(nametemp)<0){  
                    Airliner airliner = airlinerList.addAirliner();
                    airliner.setAirlinerName(airlinerName);
                    airliner.setAirlinerAddress(airlinerAddress);
                    JOptionPane.showMessageDialog(null, "Airliner successfully created");
                    break;
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Airliner has been created");
                    break;
                }
            }
    }//GEN-LAST:event_btnCreateActionPerformed

