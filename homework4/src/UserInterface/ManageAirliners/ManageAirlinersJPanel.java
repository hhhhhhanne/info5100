    private void btnDetailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetailActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblAirliners.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select a row","Warning",JOptionPane.WARNING_MESSAGE );
        }else{
            Airliner airliner = (Airliner)tblAirliners.getValueAt(selectedRow,0);
            AirlinerDetailJPanel panel = new AirlinerDetailJPanel(CardSequenceJPanel,airliner);
            CardSequenceJPanel.add("AirlinerDetailJPanel",panel);
            CardLayout layout = (CardLayout) CardSequenceJPanel.getLayout();
            layout.next(CardSequenceJPanel);
        }
    }//GEN-LAST:event_btnDetailActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblAirliners.getSelectedRow();
        if(selectedRow>=0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "would you like to delete the airliner?","Waring",dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                Airliner airliner = (Airliner) tblAirliners.getValueAt(selectedRow, 0);
                airlinerList.deleteAirliner(airliner);
                populateTable();
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Please select a row","Warning",JOptionPane.WARNING_MESSAGE );
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

