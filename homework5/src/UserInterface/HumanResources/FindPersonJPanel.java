    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String lastName = txtLastName.getText();
        if(business.getPersonDirectory().findPersonByLastName(lastName)==null)
        {
            JOptionPane.showMessageDialog(null,"cannot find this last name");
            return;
        }
        Person person = business.getPersonDirectory().findPersonByLastName(lastName);
        ViewPersonJPanel panel = new ViewPersonJPanel(person,processContainer);
        processContainer.add("ViewPersonJPanel",panel);
        CardLayout layout = (CardLayout)processContainer.getLayout();
        layout.next(processContainer);
    }//GEN-LAST:event_btnSearchActionPerformed


