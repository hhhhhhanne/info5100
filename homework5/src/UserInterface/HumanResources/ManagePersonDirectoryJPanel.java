    public void populatePersonTable(){
        int rowCount = tbPL.getRowCount();
        DefaultTableModel model = (DefaultTableModel)tbPL.getModel();
        for(int i = rowCount - 1; i >=0; i--) {
            model.removeRow(i);
        }
        for(Person person : business.getPersonDirectory().getPersonlist()) {
            Object row[] = new Object[4];
            row[0] = person;
            row[1] = person.getFirstName();
            row[2] = person.getLastName();
            row[3] = person.getAccounts();
            model.addRow(row);
        }
    }

