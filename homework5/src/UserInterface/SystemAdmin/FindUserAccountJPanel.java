    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String userName = txtUserName.getText();
        if(business.getUserAccountsDirectory().searchUserAccount(userName)==null)
        {
            JOptionPane.showMessageDialog(null,"cannot find this user name");
            return;
        }
        UserAccount ua = business.getUserAccountsDirectory().searchUserAccount(userName);
        ViewUserAccountJPanel panel = new ViewUserAccountJPanel(business, ua, processContainer);
        processContainer.add("ViewUserAccountJPanel", panel);
        CardLayout layout = (CardLayout)processContainer.getLayout();
        layout.next(processContainer);
    }//GEN-LAST:event_btnSearchActionPerformed


