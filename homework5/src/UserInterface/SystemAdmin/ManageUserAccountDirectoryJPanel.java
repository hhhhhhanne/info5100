    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int row = tbUA.getSelectedRow();
        if(row<0) {
             JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        UserAccount ua = (UserAccount)tbUA.getValueAt(row, 0);
        UpdateUserAccountStatusJPanel panel = new UpdateUserAccountStatusJPanel(business, ua, processContainer);
        processContainer.add("UpdateUserAccountStatusJPanel", panel);
        CardLayout layout = (CardLayout)processContainer.getLayout();
        layout.next(processContainer);
    }//GEN-LAST:event_btnUpdateActionPerformed

