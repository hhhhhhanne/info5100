    public void populateTable(){
        int rowCount = tbUA.getRowCount();
        DefaultTableModel model = (DefaultTableModel)tbUA.getModel();
        for(int i = rowCount - 1; i >=0; i--) {
            model.removeRow(i);
        }
        for(UserAccount ua : business.getUserAccountsDirectory().getUserAccountList()) {
            Object row[] = new Object[4];
            row[0] = ua;
            row[1] = ua.getPwd();
            row[2] = ua.getRole();
            row[3] = ua.isStatus();
            model.addRow(row);
        }
    }
