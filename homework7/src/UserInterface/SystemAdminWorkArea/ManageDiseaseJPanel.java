    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) tblDis.getModel();

        model.setRowCount(0);
        for (String disease : system.getDiseaseCatalog()) {
            Object[] row = new Object[1];
            row[0] = disease;
            model.addRow(row);
        }
    }

