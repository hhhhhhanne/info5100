    public void populateCliTable() {
        DefaultTableModel model = (DefaultTableModel) tbcli.getModel();

        model.setRowCount(0);

        for (ClinicOrganization organization : organization.getClinicOrgList()) {
            Object[] row = new Object[3];
            row[0] = organization.getOrganizationID();
            row[1] = organization;
            row[2] = organization.getWorkArea();

            model.addRow(row);

        }
    }
