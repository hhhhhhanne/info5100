    public void populateUATable(){
        DefaultTableModel model = (DefaultTableModel) userJTable.getModel();

        model.setRowCount(0);
        
            for (UserAccount ua : organization.getUserAccountDirectory().getUserAccountList()) {
                Object row[] = new Object[2];
                row[0] = ua;
                row[1] = ua.getRole();
                ((DefaultTableModel) userJTable.getModel()).addRow(row);
            }
            for(ClinicOrganization co: organization.getClinicOrgList()){
                for(UserAccount ua1 : co.getUserAccountDirectory().getUserAccountList())
                {
                    Object row2[] = new Object[2];
                    row2[0] = ua1;
                    row2[1] = ua1.getRole();
                    model.addRow(row2);
                }
            }
            for(UserAccount ua2: organization.getInventoryOrg().getUserAccountDirectory().getUserAccountList()){
                Object row3[] = new Object[2];
                row3[0] = ua2;
                row3[1] = ua2.getRole();
                model.addRow(row3);
            }
    }
