    public void populateOrderTabel() {
        DefaultTableModel model = (DefaultTableModel) orderTabel.getModel();
        model.setRowCount(0);
        
        for (Order order : bp.getOrderDirectory().getOrderDirectory()) {
            Object[] row = new Object[4];
            row[0] = order.getFrom(); 
            for(FoodInventory foodinv: order.getOffer()) {
                row[1] = foodinv.getFood();
                row[2] = foodinv.getQuantity();
                row[3] = foodinv.getFood().getPrice() * foodinv.getQuantity();
                model.addRow(row);
            }
        }
    }

