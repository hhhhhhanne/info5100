    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) enterpriseJTable.getModel();
        model.setRowCount(0);
        for (Organization org : enterprise.getOdir().getOrgDir()) {
            Object[] row = new Object[2];
            row[0] = org.getName();
            row[1] = org.getOrgType();

            model.addRow(row);
        }
    }
    
    public void populateComboBox() {
        this.typeComboBox.removeAllItems();
        for(OrganizationType orgtype: OrganizationType.values()) {
            if(this.enterprise.getType() == EnterpriseType.FoodBank && orgtype == OrganizationType.Service) {
                continue;
            }
            typeComboBox.addItem(orgtype);
        }
    }

