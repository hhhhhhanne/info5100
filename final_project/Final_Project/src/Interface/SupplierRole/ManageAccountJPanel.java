    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        Organization organization = (Organization) organizationJComboBox.getSelectedItem();
        int selectedRow=userJTable.getSelectedRow();
        if(selectedRow>=0)
        {
            Account ua=(Account)userJTable.getValueAt(selectedRow, 1);
            organization.getAccountDirectory().deleteAccount(ua);
            popData();
        }
        else{JOptionPane.showMessageDialog(null, "please select a row");}

    }//GEN-LAST:event_btndeleteActionPerformed

