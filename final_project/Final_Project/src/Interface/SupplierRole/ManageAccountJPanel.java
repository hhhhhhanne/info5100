    public void popOrganizationComboBox() {
        organizationJComboBox.removeAllItems();

        for (Organization organization : enterprise.getOdir().getOrgDir()) {
            organizationJComboBox.addItem(organization);
        }
    }
    
    public void populateEmployeeComboBox(Organization organization){
        employeeJComboBox.removeAllItems();
        
        for (P em : organization.getPersonDirectory().getPersonDir()){
            Person emp = (Person)em;
            employeeJComboBox.addItem(emp);
        }
    }


    public void popData() {

        DefaultTableModel model = (DefaultTableModel) userJTable.getModel();

        model.setRowCount(0);

        for (Organization organization : enterprise.getOdir().getOrgDir()) {
            for (Account ua : organization.getAccountDirectory().getAccountCatalog()) {
                Object row[] = new Object[3];
                row[0]= ua.getPerson();
                row[1] = ua;
                row[2] = ua.getAccountRole();
                model.addRow(row);
            }
        }
    }

