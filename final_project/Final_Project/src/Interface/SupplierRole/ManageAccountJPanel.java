    private void createUserJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createUserJButtonActionPerformed
        String username = nameJTextField.getText();
        String password = passwordJTextField.getText();
        Organization organization = (Organization) organizationJComboBox.getSelectedItem();
        Person employee = (Person) employeeJComboBox.getSelectedItem();
        Role role = new InventoryRole();
        if( nameJTextField.getText().isEmpty()||passwordJTextField.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null,"username or password cant be blank");
            return;
        }
        for(Organization og:enterprise.getOdir().getOrgDir()){
            for(Account ac:og.getAccountDirectory().getAccountCatalog()){
                if(ac.getUsername().equals(username))
                {
                    JOptionPane.showMessageDialog(null,"this account existed");
                    return;
                }
                if(ac.getPerson()==employee)
                {
                    JOptionPane.showMessageDialog(null,"this person has account");
                    return;
                }
            }
        }
        Account newacc = this.business.getAccountDirectory().addAccount(username, password, role, employee);
        if(newacc != null) {
            organization.getAccountDirectory().addAccount(newacc);
            JOptionPane.showMessageDialog(null,"add successfullys");
        }
        this.popData();

    }//GEN-LAST:event_createUserJButtonActionPerformed

