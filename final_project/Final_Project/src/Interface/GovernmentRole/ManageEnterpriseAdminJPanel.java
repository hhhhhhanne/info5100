    public void populateTabel() {
        DefaultTableModel model = (DefaultTableModel) enterpriseJTable.getModel();
        model.setRowCount(0);
        for (Enterprise enterprise : network.getGovernment().getEnterpriseDirectory().getEnterpriseDirectory()) {
            if(enterprise.getType() == EnterpriseType.FoodBank) {
                FoodBank foodbank = (FoodBank)enterprise;
                for(Account acc: foodbank.getAccountDirectory().getAccountCatalog()) {
                    Object[] row = new Object[2];
                    row[0] = enterprise.getName();
                    row[1] = acc.getUsername();
                    model.addRow(row);
                }
            }
        }
    }
    
    public void populateComboBox(){
        enterpriseJComboBox.removeAllItems();
        //System.out.println(this.network);
        for(Enterprise fb: this.network.getGovernment().getEnterpriseDirectory().getEnterpriseDirectory()) {
            if(fb.getType() == EnterpriseType.FoodBank) {
                enterpriseJComboBox.addItem(fb);
            }
        }
    }

