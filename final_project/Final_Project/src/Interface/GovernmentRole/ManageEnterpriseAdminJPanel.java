    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        Enterprise enterprise = (Enterprise)enterpriseJComboBox.getSelectedItem();
        //System.out.println(enterprise);
        String username = usernameJTextField.getText();
        String password = String.valueOf(passwordJPasswordField.getPassword());
        String name = nameJTextField.getText();
        if(enterprise == null || username.equals("") || password.equals("") || name.equals("")) {
            JOptionPane.showMessageDialog(null, "Please fill in all blanks", "Error", JOptionPane.ERROR_MESSAGE);
            return ;
        }
        //FoodBank foodbank = (FoodBank)enterprise;
        Person employee = enterprise.getPersonDirectory().addNewPerson(name);
        Account account = business.getAccountDirectory().addAccount(username, password, new FoodBankAdminRole(), employee);
        if(account != null) {
            account = enterprise.getAccountDirectory().addAccount(account);
            System.out.println("Account Add:"+enterprise.getAccountDirectory().getAccountCatalog().size());
        }
        this.populateTabel();

    }//GEN-LAST:event_btnSubmitActionPerformed

