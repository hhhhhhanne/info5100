    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        String name = txtName.getText();
        if (name.equals("")) {
            JOptionPane.showMessageDialog(null, "please input name");
            return;
        }
        for (Plan plan : government.getPlanlist().getPlanList()) {
            if (plan.getName().equalsIgnoreCase(name)) {
                JOptionPane.showMessageDialog(null, "this name exists");
                return;
            }
        }
        try {
            float income = Float.parseFloat(txtIncome.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "income format error");
            return;
        }
        try {
            float benefit = Float.parseFloat(txtBe.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "benefit format error");
            return;
        }
        float income = Float.parseFloat(txtIncome.getText());
        float benefit = Float.parseFloat(txtBe.getText());
        if (income < 0) {
            JOptionPane.showMessageDialog(null, "income format error");
            return;
        }
        if (benefit <= 0) {
            JOptionPane.showMessageDialog(null, "benefit format error");
            return;
        }
        government.getPlanlist().addPlan(name, benefit, income);
        JOptionPane.showMessageDialog(null, "create successfully");
    }//GEN-LAST:event_btnCreateActionPerformed

