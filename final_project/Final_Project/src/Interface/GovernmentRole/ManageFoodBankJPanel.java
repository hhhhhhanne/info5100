    public void populateComboBox(){
        enterpriseTypeJComboBox.removeAllItems();
        for(FoodBankType fbtype: FoodBankType.values()) {
            enterpriseTypeJComboBox.addItem(fbtype);
        }
    }
    
    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) enterpriseJTable.getModel();
        model.setRowCount(0);
        for (Enterprise enterprise : network.getGovernment().getEnterpriseDirectory().getEnterpriseDirectory()) {
            if(enterprise.getType() == EnterpriseType.FoodBank) {
                FoodBank foodbank = (FoodBank)enterprise;
                Object[] row = new Object[2];
                row[0] = foodbank;
                row[1] = foodbank.getFbtype().getValue();
                model.addRow(row);
            }
        }
    }

